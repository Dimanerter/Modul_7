from scipy.special import erf

# Задаем точность
e = 10 ** (-10)

def func(x):
    # Начальное значение ряда - первый член
    sum_series = x
    term = x  # Начальный член ряда
    i = 1     # Индекс члена ряда

    # Пока текущий член ряда больше заданной точности
    while abs(term) >= e:
        # Вычисляем следующий член
        term *= (-(2 * i - 1) * x**2) / (i * (2 * i + 1))
        # Добавляем его к сумме
        sum_series += term
        # Переходим к следующему индексу
        i += 1

    return (2 / (3.14159)**0.5) * sum_series  # Умножаем результат на коэффициент

# Проверка результата для x = 2
x = 2
print("Вычисленный результат:", func(x))
print("Результат scipy.special.erf:", erf(x))
